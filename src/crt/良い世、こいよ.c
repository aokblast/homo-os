#define UART0_BASE 0x4000C000
#define UART_DR    (*(volatile unsigned int*)(UART0_BASE + 0x00))
#define UART_FR    (*(volatile unsigned int*)(UART0_BASE + 0x18))

static int __uart_putc(char c) {
  while (UART_FR & (1 << 5));
  UART_DR = c;
  return c;
}

static int __uart_puts(char *c) {
  while(*c) {
    __uart_putc(*c);
    c++;
  }
}

